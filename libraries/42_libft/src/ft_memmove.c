#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	void	*ptr;

	if (!src && !dest)
		return (0);
	ptr = dest;
	if (src < dest)
	{
		src += n;
		dest += n;
		while (n--)
			*(char *)--dest = *(char *)--src;
	}
	else
		while (n--)
			*(char *)dest++ = *(char *)src++;
	return (ptr);
}
