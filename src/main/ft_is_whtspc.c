#include "../../includes/minishell.h"

int	ft_is_whtspc(char ch)
{
	if (ch == ' ' || ch == '\t' || ch == '\r' || ch == '\v' || \
			ch == '\f' || ch == '\n')
		return (1);
	return (0);
}
